const cvPlayerTagDiffuse = (hid, lid) => {
    const conversionChars = "0289PYLQGRJCUV";
    let fullId = (parseInt(lid) << 8) | parseInt(hid);
    let tag = "";
    while (fullId > 0) {
        const conversionCharIndex = fullId % conversionChars.length;
        tag += conversionChars[conversionCharIndex];
        fullId -= conversionCharIndex;
        fullId = Math.floor(fullId / conversionChars.length);
    }
    return tag.split("").reverse().join("");
}
const cvPlayerTag = (lid, hid, hashTag = false) => {
    const conversionChars = "0289PYLQGRJCUV";
    let id = (BigInt(hid) << 8n) | BigInt(lid), tag = "";
    while (id > 0n) {
        let index = Number(id % BigInt(conversionChars.length));
        tag = conversionChars[index] + tag;
        id /= BigInt(conversionChars.length);
    }
    if (hashTag == true) {
        return "#" + tag;
    }
    return tag;
}
